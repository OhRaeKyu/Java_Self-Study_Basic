public class variable {
	public static void main(String[] args) {
		//	Number -> integer (정수)
		int a = 1;
		System.out.println("a = " + a);
		
		//	Real Number -> double (실수)
		double b = 1.1;
		System.out.println("b = " + b);

		// String
		String c = "Hello World";
		System.out.println("c = " + c);
	}
}