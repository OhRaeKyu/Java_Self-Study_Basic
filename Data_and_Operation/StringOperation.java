public class StringOperation {
	public static void main(String[] args) {
		//	문자열 길이
		System.out.println("Hello World".length());
		
		//	문자열 대체
		System.out.println("Hello, leezche ... bye. ".replace("leezche",  "Raekyu"));
		System.out.println("Hello, [[[name]]] ... bye. ".replace("[[[name]]]",  "Raekyu"));
	}
}